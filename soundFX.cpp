/*--------------------------------------------------------*/
void successSound()
{
  for ( uint8_t n = 0; n < 200 ; n += 5 )
  {
    Sound( n, 10 );
    Sound( n + 50, 10 );
  }
}

/*--------------------------------------------------------*/
void failingSound()
{
  for ( uint8_t n = 200; n > 0; n -= 5 )
  {
    Sound( n, 10 );
    Sound( n + 50, 10 );
  }
}
